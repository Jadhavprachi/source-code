package com.project.bean;

public class URLBean {

	private Integer urlId;
	private String urlTitle;
	private String urlLink;
	private String urlConcept;
	private String urlTime;

	public Integer getUrlId() {
		return urlId;
	}

	public void setUrlId(Integer urlId) {
		this.urlId = urlId;
	}

	public String getUrlTitle() {
		return urlTitle;
	}

	public void setUrlTitle(String urlTitle) {
		this.urlTitle = urlTitle;
	}

	public String getUrlLink() {
		return urlLink;
	}

	public void setUrlLink(String urlLink) {
		this.urlLink = urlLink;
	}

	public String getUrlConcept() {
		return urlConcept;
	}

	public void setUrlConcept(String urlConcept) {
		this.urlConcept = urlConcept;
	}

	public String getUrlTime() {
		return urlTime;
	}

	public void setUrlTime(String urlTime) {
		this.urlTime = urlTime;
	}

}
